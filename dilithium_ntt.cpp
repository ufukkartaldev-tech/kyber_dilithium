static int32_t montgomery_reduce_d(int64_t a) {
    int32_t t;
    t = (int32_t)((uint32_t)a * D_QINV);
    t = (int32_t)((a - (int64_t)t * D_QINV) >> 32);
    // Dilithium için montgomery reduce biraz daha dikkatli (64-bit ara değer)
    return t;
}

// Barrett reduction alternatifi (Dilithium için)
static int32_t reduce32(int32_t a) {
    int32_t t;
    t = (a + (1 << 22)) >> 23;
    t = a - t * D_QINV;
    return t;
}

static int32_t fqmul_d(int32_t a, int32_t b) {
    return montgomery_reduce_d((int64_t)a * b);
}

void dilithium_ntt(int32_t a[256]) {
    unsigned int len, start, j, k;
    int32_t zeta;

    k = 1;
    for (len = 128; len >= 1; len >>= 1) {
        for (start = 0; start < 256; start = j + len) {
            zeta = zetas_d[k++];
            for (j = start; j < start + len; j++) {
                int32_t t = fqmul_d(zeta, a[j + len]);
                a[j + len] = a[j] - t;
                a[j] = a[j] + t;
            }
        }
    }
}

void dilithium_invntt(int32_t a[256]) {
    unsigned int len, start, j, k;
    int32_t zeta;

    k = 255;
    for (len = 1; len <= 128; len <<= 1) {
        for (start = 0; start < 256; start = j + len) {
            zeta = -zetas_d[k--];
            for (j = start; j < start + len; j++) {
                int32_t t = a[j];
                a[j] = a[j] + a[j + len];
                a[j + len] = t - a[j + len];
                a[j + len] = fqmul_d(zeta, a[j + len]);
            }
        }
    }

    // Normalization: x * (n^-1 * 2^32) mod q
    // 256^-1 mod q * 2^32 mod q = 4190209 (ön hesaplanmış)
    for (j = 0; j < 256; j++) {
        a[j] = fqmul_d(a[j], 4190209);
    }
}
