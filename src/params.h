#ifndef PARAMS_H
#define PARAMS_H

#define KYBER_N 256
#define KYBER_Q 3329

// Bu değerler Kyber seviyelerine göre değişir.
// Kyber-512 (Level 1): K=2
// Kyber-768 (Level 2): K=3

#define KYBER_512_K 2
#define KYBER_768_K 3

#define KYBER_SYMBYTES 32
#define KYBER_SSBYTES 32

#define KYBER_POLYBYTES 384
#define KYBER_POLYVECBYTES(k) (k * KYBER_POLYBYTES)

// Kyber-512 (K=2)
#define KYBER_512_ETAl 3
#define KYBER_512_ETA2 2
#define KYBER_512_PUBLICKEYBYTES (KYBER_POLYVECBYTES(2) + KYBER_SYMBYTES)
#define KYBER_512_SECRETKEYBYTES (KYBER_POLYVECBYTES(2) + KYBER_512_PUBLICKEYBYTES + 2 * KYBER_SYMBYTES)
#define KYBER_512_CIPHERTEXTBYTES (320 * 2 + 128) // (du*k*n/8 + dv*n/8) -> du=10, dv=4

// Kyber-768 (K=3)
#define KYBER_768_ETAl 2
#define KYBER_768_ETA2 2
#define KYBER_768_PUBLICKEYBYTES (KYBER_POLYVECBYTES(3) + KYBER_SYMBYTES)
#define KYBER_768_SECRETKEYBYTES (KYBER_POLYVECBYTES(3) + KYBER_768_PUBLICKEYBYTES + 2 * KYBER_SYMBYTES)
#define KYBER_768_CIPHERTEXTBYTES (320 * 3 + 128) // du=10, dv=4

#endif
